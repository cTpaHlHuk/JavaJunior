package Lesson2;

public class SwitchCase {
    public static void main(String[] args) {
        int ocenca = 4;

        switch (ocenca) {
            case 5:
                System.out.println("Молодец");
                break;
            case 4:
                System.out.println("Нормально");
                break;
            case 3:
                System.out.println("Троишник");
                break;
            case 2:
                System.out.println("Опять двойка");
                break;
            case 1:
                System.out.println("Неси ремень");
                break;
        }
    }
}
